public class if_else {

    public static void main(String[] args){
        int iCounter = 5;

        if(iCounter == 5) {
            System.out.println("iCounter is 5.");
        }
        else if (iCounter == 6) {
            System.out.println("iCounter is 6.");
        }
        else {
            System.out.println("Else Statement triggered.");
        }
    }
}